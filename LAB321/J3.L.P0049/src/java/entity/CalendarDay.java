package entity;

/**
 *
 * @author Kien
 */
public class CalendarDay {
    private String day;
    private String month;
    private String year;
    private String color;

    public CalendarDay(String day, String month, String year, String color) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.color = color;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
    
}
