    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        try {
            Calendar c = Calendar.getInstance();
            ArrayList<CalendarDate> list = CalendarManager.getCalendar(c.get(Calendar.YEAR), c.get(Calendar.MONTH));
            String month = new SimpleDateFormat("MMM === yyyy", Locale.ENGLISH).format(c.getTime());
            int cm = c.get(Calendar.MONTH);
            int cy = c.get(Calendar.YEAR);
            request.setAttribute("cm", cm);
            request.setAttribute("cy", cy);
            request.setAttribute("month", month);
            request.setAttribute("calendar", list);
            request.getRequestDispatcher("jsp/display.jsp").forward(request, response);
        } catch (Exception e) {
        }
    }

